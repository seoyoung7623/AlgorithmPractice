'''
모든 타일은 1 or 00으로 이루어짐
이게 dp라고..?

1
00 11
100, 111, 001
0000 1100 1001 1111 0011
10000 11100 00100 00001 11001 10011 11111 00111

중복 처리를 위헤 set()을 사용..?
'''
N = int(input())

dp = [0]*(N+2) #N이 1인경우 dp[2]에 값을 할당하기 위해서!
dp[1] = 1
dp[2] = 2

for i in range(3,N+1):
    dp[i] = (dp[i-2] + dp[i-1])%15746
print(dp[N])
